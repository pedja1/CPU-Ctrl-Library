package rs.pedjaapps.cpuctrllibrary;

public final class TimesInState
{

	private final int freq;
	private final long time;

	public TimesInState(final int freq, final long time)
	{
		this.freq = freq;
		this.time = time;
		

	}


	public int getFreq()
	{
		return freq;
	}


	public long getTime()
	{
		return time;
	}

	

}